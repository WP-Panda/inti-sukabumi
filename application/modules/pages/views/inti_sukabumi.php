                <h3 class="text-bold">Perhimpunan INTI Kota Sukabumi periode	2015 – 2019 terdiri dari :</h3>
                <p>
                  Dewan Pakar, Dewan Kehormatan dan Dewan Penasehat
                </p>
                <p>
                  Dewan Pengurus yang terdiri dari seorang Ketua, 4 Orang Wakil Ketua, 3 Orang Sekretaris dan 3 Orang Bendahara
                </p>
                <p>
                  Wira-Usaha, Dokumentasi & IT, Organisasi dan Kaderisasi ; Kesehatan, Sosial Komunikasi dan Humas ; Pemuda & Olah Raga, Hukum & Advokasi dan Parawisata ; Pendidikan, Seni & Budaya dan Pemberdayaan Perempuan
                </p>
                <p>
                  Alamat Sekretariat sementara : Gedung Perkumpulan Warga Yong Chun Sukabumi, Ruko Danalaga Square Blok G No. 10, Jl. Pejagalan ( Odeon ) – Kota Sukabumi
                </p>
                <p>
                  <b>Bpk. Titih Sugandi</b> menjabat sebagai Ketua Perhimpunan INTI Kota Sukabumi berharap melalui wadah Perhimpunan INTI Kota Sukabumi ini, dapat mempersatukan Warga Tionghoa Sukabumi tanpa membeda-bedakan ( lintas ) Suku, Agama, Perkumpulan, Status dan Golongan. Serta memotivasi agar etnis Tionghoa tidak saja bergelut dibidang usaha atau perdagangan, tapi bisa pula bergelut dibidang seperti pendidikan, sosial dan lain sebagainya
                </p>
                <p>
                  Selanjutnya Ketua Perhimpunan INTI Sukabumi menyusun Kepengurusan Dewan Pimpinan Cabang Perhimpunan INTI Kota Sukabumi dengan melibatkan Tokoh di Sukabumi, Perwakilan setia Perkumpulan dan Agama serta Pemuda – Pemudi Tionghoa sebagai bagian dari kontribusi positif dan berorientasi untuk memajukan pembangunan daerah Sukabumi dengan semangat kebangsaan.
                </p>
                <h3 class="text-bold">
                  Agenda Kerja Triwulan Pertama
                </h3>
                <ol>
                  <li>Dibidang sosial, membantu mengumpulkan Warga Tionghoa Sukabumi yang terkena musibah kebakaran di pusat perdagangan Pasar Pelita dan membantu memfasilitasi pertemuan dengan Walikota & Wakil Walikota Sukabumi supaya hak dan harapan-harapan sebagai pengusaha diperhatikan, salah satu hasil perjuangan bersama yaitu “ Pasar Marema “ di bulan puasa yang setiap tahunnya dijalankan di Jl. Pelabuan, mulai tahun 2016 dilarang</li>
                  <li>Dibidang sosial, dengan mengadakan Aksi Donor Darah pada tanggal dengan bekerja-sama dengan Ren Ai,   PMI Kota Sukabumi, RS. Kartika Sukabumi dan Perkumpulan Warga Yong Chun Sukabumi</li>
                  <li>
                    Dibidang Pendidikan dan Usaha : mengadakan Seminar Perpajakan untuk Pengusaha – Pengusaha Warga Tionghoa Sukabumi, yang akan dilaksanakan pada tgl. 16 Januari 2016, kegiatan lain yang berjalan diantaranya
                    <ol>
                      <li>Les Bahasa Mandarin gratis</li>
                      <li>Les Komputer gratis</li>
                      <li>Karaoke di Sekretariat gratis</li>
                      <li>Kegiatan Olah Raga</li>
                      <li>Kegiatan Barongsai</li>
                      <li>Seminar – Seminar, dan lain-lain</li>
                    </ol>
                  </li>
                  <li>Mengadakan Acara Pelantikan Dewan Pengurus Cabang Perhimpunan INTI Sukabumi Periode 2015 – 2020 oleh Dewan Pengurus Pusat dan atau Dewan Pengurus Daerah Provinsi Jawa Barat waktu dan tempat serta Acara akan dijadwalkan bersama.</li>
                  <li>Mengadakan Perayaan IMLEK 1567 INTI Sukabumi, pada tanggal 21 Februari 2016.</li>
                </ol>
                <h3 class="text-bold">Program Perhimpunan INTI Kota Sukabumi 2015 - 2019,</h3> diantaranya :
                <ol>
                  <li>Memiliki Gedung Sekretariat Perhimpunan Indonesia Tionghoa Kota Sukabumi</li>
                  <li>b.Membuat museum foto documenter tokoh, tempat dll Kota Sukabumi jaman dahulu hingga terkini. </li>
                  <li>c.Membangun Krematorium di Kota Sukabumi</li>
                  <li>d.Bidang Usaha dan Konsultan bidang Hukum</li>
                  <li>e.Membangun Sekolah Bahasa, memberikan Program beasiswa, dan lain – lain.</li>
                </ol>
