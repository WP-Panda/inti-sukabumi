        <div class="row news-detail">
        <br><br><p>Didirikan di Jakarta di hadapan Notaris James Herman Rahardjo pada 5 Februari 1999&nbsp; oleh 18 Warga Negara Republik Indonesia&nbsp; yang peduli terhadap penyelesaian “Masalah Tionghoa”.<br>
<br>
Perhimpunan INTI adalah Perhimpunan INTI adalah organisasi yang bersifat kebangsaan sesuai semangat Mukadimah UUD RI 1945, bebas, egaliter, pluralis, inklusif, demokratis, tidak bernaung atau mengikatkan diri kepada salah satu partai politik dan terbuka bagi semua Warga Negara Indonesia yang setuju pada Anggaran Dasar, Anggaran Rumah Tangga, serta Tujuan Perhimpunan INTI.<br>
<br>
Menyadari sepenuhnya bahwa “Masalah Tionghoa” di Indonesia merupakan warisan sejarah kolonial yang telah membebani perjalanan sejarah bangsa Indonesia selama ini, Perhimpunan INTI didirikan dengan tujuan menjadi organisasi yang maju, modern, bercitra internasional, berorientasi pada Kebangsaan Indonesia, menghargai hak asasi manusia, egaliter, pluralis, inklusif, demokratis, dan transparan untuk berperan aktif dalam dinamika proses pembangunan bangsa (nation building), antara lain menyelesaikan “Masalah Tionghoa” di Indonesia, menuju terwujudnya Kebangsaan Indonesia yang kokoh, rukun bersatu dalam keharmonisan, bhinneka, saling menghargai, dan saling percaya.<br>
<br>
Perhimpunan INTI berkeyakinan usaha penyelesaian “Masalah Tionghoa” di Indonesia harus diletakkan di atas landasan usaha penyelesaian seluruh permasalahan nasional yang tengah dihadapi Bangsa dan Negara Indonesia; dan bahwa pengikutsertaan seluruh WNRI Keturunan Tionghoa secara menyeluruh, bulat, dan utuh adalah syarat mutlak penyelesaian “Masalah Tionghoa” di Indonesia.<br>
<br>
Dideklarasikan pada 10 April 1999 yang kemudian ditetapkan sebagai Hari Jadi-nya, per Februari 2007 telah terbentuk 12 Pengurus Daerah&nbsp; (tingkat Provinsi) dan 38 Pengurus Cabang&nbsp; (tingkat Kota/Kabupaten) Perhimpunan INTI di seluruh Indonesia.<br>
<br>
Sebagai hasil Musyawarah Nasional II pada Desember 2009 secara aklamasi mendaulat Pengurus Pusat dengan masa bakti 2009-2013 dipimpin kembali oleh Rachman Hakim dan Budi S. Tanuwibowo selaku Ketua Umum dan Sekretaris Jenderal, melanjutkan kepengurusan mereka pada masa bakti 2005-2009. Sebelumnya, Pengurus Pusat Perhimpunan INTI masa bakti 1999-2005 dipimpin oleh Drs. Eddie Lembong dan Drs. Michael Utama Purnama, MA masing-masing sebagai Ketua Umum dan Sekretaris Jenderal.<br>
<br>
Dasar Pemikiran didirikannya Perhimpunan INTI adalah:<br>
1.&nbsp;&nbsp;&nbsp; Bahwa dilahirkan sebagai suatu etnis bukan merupakan suatu pilihan, melainkan anugerah dari Tuhan Yang Maha Esa.<br>
2.&nbsp;&nbsp;&nbsp; Sejarah mencatat, warga Tionghoa telah berada di Nusantara sejak berabad-abad yang lalu dan ikut serta memperkaya khazanah bumi pertiwi dalam berbagai aspek kehidupan, di antaranya (meliputi) bidang agama, sosial budaya, politik, ekonomi dan perdagangan.<br>
3.&nbsp;&nbsp;&nbsp; Bahwa lahirnya Republik Indonesia merupakan hasil perjuangan seluruh rakyat Indonesia termasuk warga Tionghoa, oleh karena itu warga Tionghoa adalah bagian integral bangsa Indonesia.<br>
4.&nbsp;&nbsp;&nbsp; Penyelenggaraan kehidupan berbangsa dan bernegara seharusnya dijalankan dan didasari oleh jiwa dan roh mukadimah UUD 1945, sehingga penyelesaian setiap permasalahan bangsa didasari oleh semangat kebangsaan.<br>
5.&nbsp;&nbsp;&nbsp; Warga Tionghoa bertekad ikut serta dalam pembangunan bangsa yang lebih bersatu, demokratis, adil dan makmur, guna menghantarkan bangsa Indonesia menuju masyarakat dunia yang lebih bermartabat, damai dan sejahtera.<br>
