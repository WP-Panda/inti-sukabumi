					<br><br><p><strong><em>Pendahuluan</em></strong></p>

<p>Memenuhi salah satu amanat Musyawarah Nasional I Perhimpunan INTI, 9-11 Desember 2005 di Jakarta, pada 22-23 April 2006 di Jakarta diselenggarakanlah Musyawarah Nasional Luar Biasa (MUNASLUB) Amendemen Anggaran Dasar &amp; Anggaran Rumah Tangga Perhimpunan INTI.</p>

<p>Selain menyepakati dan menghasilkan amendemen Anggaran Dasar &amp; Anggaran Rumah Tangga Perhimpunan INTI, MUNASLUB dimaksud juga menyepakati bahwa di masa mendatang segala inventaris dan aset kekayaan baik bergerak maupun tidak bergerak yang diperoleh Pengurus Pusat/Pengurus Daerah/Pengurus Cabang/Pengurus Kecamatan akan diatasnamakan Perhimpunan INTI. Untuk memenuhinya, Perhimpunan INTI harus terdaftar sebagai Perkumpulan Berbadan Hukum (<em>Rechtspersoonlijkheid van Vereenigingen</em>).</p>

<p>Maka dibawalah Anggaran Dasar &amp; Anggaran Rumah Tangga Perhimpunan INTI hasil MUNASLUB dimaksud ke Departemen Hukum dan Hak Asasi Manusia untuk mendapat pengesahan sebagai Perkumpulan Berbadan Hukum.</p>

<p>Setelah ditelaah, untuk mendapatkan status Perkumpulan Berbadan Hukum, melalui surat tertanggal 4 Januari 2007 No. C2-HT.01.03.A-343, Departemen Hukum dan Hak Asasi Manusia memberikan sejumlah pertimbangan agar Anggaran Dasar Perhimpunan INTI memenuhi persyaratan sebagai anggaran dasar Perkumpulan Berbadan Hukum.</p>

<p>Dengan latar belakang itu, terutama untuk memenuhi amanat MUNASLUB dimaksud, maka keputusan MUNASLUB tentang amendemen anggaran dasar pun disesuaikan dengan pertimbangan Departemen Hukum dan Hak Asasi Manusia, dan dituangkan dalam akta No. 01 tanggal 6 Januari 2007 tentang Perubahan Anggaran Dasar Perhimpunan Indonesia Tionghoa dibuat di hadapan Notaris Djuliana, SH berkedudukan di Jakarta.</p>

<p>Penyesuaian itu tentu saja berpengaruh terhadap keputusan MUNASLUB tentang amendemen anggaran rumah tangga, maka diterbitkanlah akta No. 01 tanggal 11 Mei 2007, dibuat di hadapan Notaris Djuliana, SH berkedudukan di Jakarta, yang memuat Anggaran Rumah Tangga Perhimpunan INTI hasil amendemen MUNASLUB setelah disesuaikan dengan Akta Notaris Djuliana, SH, berkedudukan di Jakarta, No. 1 tanggal 6 Januari 2007 tentang Perubahan Anggaran Dasar&nbsp;Perhimpunan Indonesia Tionghoa.</p>

<p>Pada 9 Februari 2007, Direktorat Jenderal Administrasi Hukum Umum atas nama Menteri Hukum dan Hak Asasi Manusia melalui surat keputusan No. C-11.HT.01.03.TH.2007 memberikan pengesahan Akta Pendirian Perhimpunan Indonesia Tionghoa; menyusul pengumuman Anggaran Dasar Perhimpunan INTI dalam Tambahan Berita Negara RI tanggal 27 Maret 2007 No. 25.</p>

<p>Dengan pengumuman dalam Tambahan Berita Negara RI ini, maka tuntaslah sudah proses pendaftaran Perhimpunan INTI sebagai Perkumpulan Berbadan Hukum. Sebuah proses yang ternyata cukup memakan waktu. Sampai saat diterimanya konfirmasi telah terbitnya Tambahan Berita Negara RI dimaksud pada awal November 2007, praktis 20 bulan waktu yang dibutuhkan untuk menempuh proses tersebut.</p>

<p>Atas tuntasnya proses pendaftaran Perhimpunan INTI sebagai Perkumpulan Berbadan Hukum, kami haturkan terima kasih dan penghargaan setinggi-tingginya kepada Notaris Dr. Misahardi Wilamarta, SH, Notaris Djuliana, SH, Lorenz Gunadi, SH, MH, dan Rudy, SH yang telah mencurahkan waktu, tenaga, perhatian, dan pemikiran selama proses pendaftaran ini.</p>

<p>Demikian sedikit uraian dari kami, semoga bermanfaat.</p>

